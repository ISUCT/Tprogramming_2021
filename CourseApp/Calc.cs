namespace CourseApp
{
    public class Calculator
    {
        public double Division(double a, double b)
        {
            return a / b;
        }

        public double Sum(double a, double b)
        {
            return a + b;
        }

        public double Subtraction(double a, double b)
        {
            return a - b;
        }

        public double Multiplying(double a, double b)
        {
            return a * b;
        }
    }
}