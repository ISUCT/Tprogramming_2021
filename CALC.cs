namespace CourseApp
{
    public class Calc
    {
        public int GetSum(int a, int b)
        {
            return a + b;
        }

        public double GetSum(double a, double b)
        {
            return a + b;
        }

        public int GetProduct(int a, int b)
        {
            return a * b;
        }

        public double GetQuotient(double a, double b)
        {
            return a / b;
        }
    }
}